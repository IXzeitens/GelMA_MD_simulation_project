# Default figure set — M4 is a paper-narrative executive summary, generated
# only on explicit request (`python plot_merged.py --figs M4`) so the default
# protocol-QC run stays focused on M1/M2/M3/M3b.
DEFAULT_FIGS = ("M1", "M2", "M3", "M3B")


def main() -> int:
    global _OUT_SUFFIX, _REP_FILTER
    ap = argparse.ArgumentParser()
    ap.add_argument("--figs", nargs="+", default=list(DEFAULT_FIGS),
                    help=f"Subset of figures to generate. Default: {' '.join(DEFAULT_FIGS)}. "
                         f"Use 'M4' explicitly for the selected-metrics scatter view; "
                         f"M1P/M2P/M3P for per-replica paired bars.")
    ap.add_argument("--rep", default=None,
                    help="Render single-replica figures from one replica's per-frame "
                         "CSVs (e.g. --rep rep1 or --rep rep2). Figures saved with a "
                         "'_<rep>' filename suffix; RDF panels use that replica only. "
                         "Default: ensemble (reads pre_crosslink_summary.csv).")
    args = ap.parse_args()

    apply_paper_style()

    if args.rep:
        _REP_FILTER = args.rep
        _OUT_SUFFIX = f"_{args.rep}"
        df = _build_rep_summary(args.rep)
        if df.empty:
            print(f"  [error] no per-frame CSVs found for {args.rep}")
            return 1
        print(f"built single-replica summary for {args.rep}: {len(df)} rows "
              f"(figures get '{_OUT_SUFFIX}' suffix)")
    else:
        df = _load_summary()
        print(f"loaded {len(df)} rows from {SUMMARY_CSV.name}")

    for tag in args.figs:
        fn = FIGURES.get(tag.upper())
        if fn is None:
            print(f"  [skip] unknown figure tag: {tag}")
            continue
        fn(df)
    return 0


