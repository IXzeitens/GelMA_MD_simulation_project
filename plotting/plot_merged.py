def _read_box_edge_A(sys_name: str) -> float | None:
    """Return the equilibrated cubic box edge (Å) from the latest .xsc file.

    Falls back to ``calculated_box_L_Angstrom`` in config.json if the .xsc
    is missing (e.g. mid-pipeline). Returns None if neither source works.
    """
    sys_root = DATA_ROOT.parent / sys_name
    xsc_files = sorted(sys_root.glob("Output/system_npt_part*.xsc"))
    if xsc_files:
        try:
            with xsc_files[-1].open() as f:
                last = [ln for ln in f if not ln.startswith("#")][-1]
            return float(last.split()[1])  # a_x = cubic edge
        except Exception:
            pass
    # Fallback: config.json nominal value
    cfg = sys_root / "config.json"
    if cfg.exists():
        import json
        with cfg.open() as f:
            return float(json.load(f).get("calculated_box_L_Angstrom", float("nan")))
    return None


def figure_M3b_RDF_detail(df: pd.DataFrame) -> None:
    """Per-DS MA–MA radial distribution panels, Chiu-style.

    One panel per MA-bearing system (DS = 33 / 67 / 100%), single solid
    line per panel, clean axes (no gridlines, no overlay), x-range 0–10 Å.
    Mirrors Chiu 2026 Fig 3-12 layout (rows × cols = concentrations × DS;
    here we have only one concentration so a single row of three panels).

    Cumulative coordination number N(7 Å) per reference C8 is reported
    as small inset text in each panel to keep the Polymatic-threshold
    reading visible without a separate cumulative panel.
    """
    out = _fig_path("Figure_M3b_RDF_detail")

    R_MAX = 10.0
    POLYMATIC_R = 7.0
    N_CHAINS = 12
    # n MA reactive C8 atoms PER CHAIN for each DS: 1 LMA / chain × {1,2,3}
    n_ma_per_chain_by_ds = {33: 1, 67: 2, 100: 3}

    ma_systems = sorted(
        ((spec.ds_pct, name) for name, spec in SYSTEMS.items() if spec.has_ma),
        key=lambda x: x[0],
    )
    n_panels = len(ma_systems)
    if n_panels == 0:
        return

    fig, axes = plt.subplots(
        1, n_panels, figsize=(4.0 * n_panels, 4.0), squeeze=False, sharey=False,
    )
    axes_flat = axes.flatten()

    for ax, (ds, sys_name) in zip(axes_flat, ma_systems):
        rdf = _load_ma_rdf_ensemble(sys_name)
        if rdf is None:
            ax.text(0.5, 0.5, "no data", ha="center", va="center",
                    transform=ax.transAxes, color="grey")
            ax.set_xticks([]); ax.set_yticks([])
            continue
        r, g = rdf
        mask = r <= R_MAX
        colour = DS_COLORS.get(ds, "#5a9fd4")

        # Chiu-style: single solid blue line, no gridlines, no cutoff markers
        ax.plot(r[mask], g[mask], color=colour, lw=1.6)

        # Compute N(7 Å). Density factor uses **partner pool** density
        # (other-chain MA atoms only), not total MA atoms. For 12 chains with
        # m MA per chain, each reference C8 has (12−1)·m candidate partners
        # in V_box. Using the total-MA density would over-count by ~9%.
        L = _read_box_edge_A(sys_name)
        n_ma_per = n_ma_per_chain_by_ds.get(ds)
        n7_text = None
        if L and n_ma_per:
            n_partner = (N_CHAINS - 1) * n_ma_per
            rho = n_partner / (L ** 3)
            integrand = 4.0 * np.pi * r ** 2 * g
            Nr = rho * np.array([
                np.trapezoid(integrand[: i + 1], r[: i + 1]) for i in range(len(r))
            ])
            idx7 = int(np.searchsorted(r, POLYMATIC_R))
            n7_text = f"$N(7\\,\\mathrm{{\\AA}})$ = {float(Nr[idx7]):.2f}"

        # Subtle 7 Å reference: dotted, no label
        ax.axvline(POLYMATIC_R, color="#bbb", lw=0.6, ls=":", zorder=0)

        ax.set_xlim(0, R_MAX)
        ax.set_xlabel("distance (Å)")
        ax.set_ylabel("RDF")
        ax.set_title(f"{sys_name} (DS = {ds}%)",
                     loc="center", fontsize=11, fontweight="bold", pad=8)

        # Fully enclosed frame (all 4 spines), matches NVT_Convergence look.
        # Overrides apply_paper_style's global top/right=off for these QC-style
        # panels where a closed box reads more like an instrument trace.
        for s in ("top", "right", "bottom", "left"):
            ax.spines[s].set_visible(True)
            ax.spines[s].set_linewidth(0.8)
        ax.tick_params(direction="out", length=4, width=0.6)

        # Small N(7 Å) inset in upper-right corner of each panel
        if n7_text:
            ax.text(0.97, 0.97, n7_text, transform=ax.transAxes,
                    ha="right", va="top", fontsize=9, color="#333",
                    bbox=dict(facecolor="white", edgecolor="none",
                              alpha=0.85, boxstyle="round,pad=0.3"))

    fig.suptitle("MA–MA radial distribution (vinyl C8–C8)",
                 fontsize=13, fontweight="bold", y=1.00)
    fig.tight_layout(rect=(0, 0.06, 1, 0.97))

    save_fig(fig, out)
    plt.close(fig)
    print(f"  → {out.relative_to(out.parents[1])}")


