"""Cross-system merged comparison figures (Chiu-style: DS on x-axis,
uniform fill, panel-letter labels, strong top title).

Reads `production/plot/pre_crosslink_summary.csv` (written by data_collect.py)
and produces paper-style merged figures. Default set (M1–M3) is the
protocol-QC view; M4 is an opt-in paper executive summary.

    Figure_M1_Conformational.{png,pdf}   Rg / Ree / Lp / RMSD vs DS
    Figure_M2_Interactions.{png,pdf}     4 H-bond categories + salt bridges
                                         + inter-residue contacts
    Figure_M3_MAstructure.{png,pdf}      MA-SASA + MA-MA NN + MA-MA g(r);
                                         cluster count/size/Rg dropped
                                         (cutoff-sensitive)
    Figure_M3b_RDF_detail.{png,pdf}      MA-MA g(r) + cumulative N(R)
                                         with 5 Å / 7 Å markers
    Figure_M4_vsDS.{png,pdf}             selected scatter view (opt-in only)

Style intentionally mirrors `Figure_C1_Conformation` and `Figure_C2_HBonds`
(generated by legacy `compare_plot.py`) so both series can sit next to each
other in the paper without restyling:

  * x-axis = DS% (0 / 33 / 67 / 100), one bar per system
  * one uniform pale fill colour per panel (Chiu Fig 2 style); future
    concentration scan will add side-by-side grouped bars in another colour
  * panel subtitle "(a) ..." left-aligned, bold
  * single strong top title "<Group> vs DS [× Concentration]"
  * non-stationary bars overlaid with diagonal hatching + side annotation

Run `data_collect.py` first if `pre_crosslink_summary.csv` doesn't exist.

Usage:
    python plot_merged.py               # default: M1 + M2 + M3
    python plot_merged.py --figs M1     # subset
    python plot_merged.py --figs M1 M4  # include opt-in M4
"""
